package main

import "fmt"

// Values: Go's types
// Strings, Integers, Floats, Booleans

func main() {
	// String
	fmt.Println("Hello" + "World")
	// Integers
	fmt.Println(1 + 1)
	// Float
	fmt.Println(7.0 / 3.0)
	// Booleans
	fmt.Println(true)
	// Operators
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
